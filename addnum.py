

def main():
    a=int(input('a:'))
    b=int(input('b:'))
#addition
    print("a + b = ",a+b)

#subraction
    print("a - b = ",a-b)

#multiplication
    print("a * b = ",a*b)

#for floating point division
    print("a / b = ",a/b)

#for integer(floor) division
    print("a // b = ",a//b)



if __name__ == '__main__':
    main()
